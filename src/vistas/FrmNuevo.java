    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        String jugador1= txtJugador1.getText().trim(); // Texto jugador 1
        String jugador2= txtJugador2.getText().trim(); // Texto jugador 2
        int catego= cmbCatego.getSelectedIndex();
        if(jugador1.length()>0&&jugador2.length()>0&&catego!=0){
           
            m1.setJugador1(jugador1);
            m1.setJugador2(jugador2);
            m1.setCatego(catego);
            m1.setBanNuevo(true);
            this.setVisible(false);
        }else{
            JOptionPane.showMessageDialog(this, "Datos incompletos!!");
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

